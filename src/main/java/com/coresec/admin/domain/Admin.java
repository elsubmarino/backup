package com.coresec.admin.domain;

public class Admin {
	private String f_idno;
	private String f_passwd;
	private String f_name;

	public String getF_idno() {
		return f_idno;
	}

	public void setF_idno(String f_idno) {
		this.f_idno = f_idno;
	}

	public String getF_passwd() {
		return f_passwd;
	}

	public void setF_passwd(String f_passwd) {
		this.f_passwd = f_passwd;
	}

	public String getF_name() {
		return f_name;
	}

	public void setF_name(String f_name) {
		this.f_name = f_name;
	}

}
